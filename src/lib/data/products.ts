// Helper functions
export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getProductsByUniverse(universeId: string): Product[] {
  return products.filter((p) => p.universeId === universeId);
}

export function getProductsByCategory(category: string): Product[] {
  return products.filter((p) => p.category === category);
}

export function getBestsellers(): Product[] {
  return products.filter((p) => p.isBestseller);
}

export function getNewArrivals(): Product[] {
  return products.filter((p) => p.isNew);
}

export function getFeaturedProducts(limit: number = 8): Product[] {
  return products.slice(0, limit);
}

export function getSpecials(): Product[] {
  return products.filter((p) => p.category === "specials");
}
